package operator;

public class RelationalOperator {

	public static void main(String[] args) {
		int X=10;
		int Y=20;
		System.out.println(X<Y);//True
		System.out.println(X<=Y);//True
		System.out.println(Y>50);//False
		System.out.println(Y>=10);//True
		System.out.println(X==Y);//False
		System.out.println(X!=Y);//True
	}

}
